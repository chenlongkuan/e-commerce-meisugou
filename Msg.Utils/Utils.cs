        /// <summary>
        /// 截取字符串
        /// </summary>
        /// <param name="input"></param>
        /// <param name="length"></param>
        /// <param name="postfix"></param>
        /// <returns></returns>
        public static string CutStr(object input, int length, string postfix)
        {

            Regex regex = new Regex("[^\x00-\xff]", RegexOptions.Compiled);

            //取得后缀长度
            char[] pxChars = postfix.ToCharArray();
            int pxLength = 0;
            for (int i = 0; i < pxChars.Length; i++)
            {
                if (regex.IsMatch(pxChars[i].ToString()))
                    pxLength += 2;
                else
                    pxLength += 1;
            }

            //取得输入字符长度
            char[] pChars = input.ToString().ToCharArray();
            int pLength = 0;
            for (int i = 0; i < pChars.Length; i++)
            {
                if (regex.IsMatch(pChars[i].ToString()))
                    pLength += 2;
                else
                    pLength += 1;
            }

            int templen = 0;
            string temppx = "";

            if (pLength <= length)
            {
                templen = length;
                temppx = "";
            }
            else
            {
                templen = length - pxLength;
                temppx = postfix;
            }

            //根据指定长度截取字符串
            char[] iChars = input.ToString().ToCharArray();
            int iLength = 0;
            System.Text.StringBuilder sb = new StringBuilder();
            for (int i = 0; i < iChars.Length; i++)
            {
                if (regex.IsMatch(iChars[i].ToString()))
                    iLength += 2;
                else
                    iLength += 1;

                sb.Append(iChars[i]);

                if (iLength >= length)
                    break;
            }

            //返回截取后字符串
            string result = sb.ToString();
            result += temppx;

            //过滤换行
            result = result.Replace("<br/>", "");
            return result;
        }

