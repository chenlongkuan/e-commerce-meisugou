        /// </summary>
        /// <param name="url">url</param>
        /// <returns></returns>
        public static string RequestUrlByGet(string url, string method = "GET", string encoding = "utf-8")
        {
            string json = null;
            WebRequest req = WebRequest.Create(url);
            req.Method = method;
            WebResponse res = req.GetResponse();
            Stream receiveStream = res.GetResponseStream();
            Encoding encode = Encoding.GetEncoding(encoding);
            if (receiveStream != null)
            {
                StreamReader sr = new StreamReader(receiveStream, encode);
                char[] readbuffer = new char[256];
                int n = sr.Read(readbuffer, 0, 256);
                while (n > 0)
                {
                    string str = new string(readbuffer, 0, n);
                    json += str;
                    n = sr.Read(readbuffer, 0, 256);
                }
            }
            return json;
        }


        /// <summary>
        /// 把json 数据转换成 key value格式
        /// </summary>
        /// <param name="json_code">格式如callback( {"client_id":"YOUR_APPID","openid":"YOUR_OPENID"} ); </param>
        /// <returns></returns>
        public static NameValueCollection ParseJson(string json_code)
        {
            NameValueCollection mc = new NameValueCollection();
            Regex regex = new Regex(@"(\s*\""?([^""]*)\""?\s*\:\s*\""?([^""]*)\""?\,?)");
            json_code = json_code.Trim();
            if (json_code.StartsWith("{"))
            {
                json_code = json_code.Substring(1, json_code.Length - 2);
            }
            foreach (Match m in regex.Matches(json_code))
            {
                var cstr = m.Groups[3].Value.Trim();
                cstr = cstr.Replace(",", "");
                mc.Add(m.Groups[2].Value, cstr);
            }
            return mc;
        }

